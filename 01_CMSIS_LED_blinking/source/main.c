/* CMSIS */
#include "stm32f4xx.h"


/******************************************************************************/
/* Definitions ****************************************************************/
/******************************************************************************/
#define LED_TOGGLE_DELAY    160000u


/******************************************************************************/
/* Main ***********************************************************************/
/******************************************************************************/
int main(void) {
    /* Enable GPIOD clock */
    RCC->AHB1ENR |= RCC_AHB1ENR_GPIODEN;
    /* GPIOD pin 14 in push-pull mode */
    GPIOD->MODER |= GPIO_MODER_MODER14_0;

    while (1) {
        /* Toggle GPIOD pin 14 */
        GPIOD->ODR ^= GPIO_ODR_ODR_14;
        /* Toggle delay */
        for (volatile int i = 0; i < LED_TOGGLE_DELAY; ++i) {
            ;
        }
    }
}
